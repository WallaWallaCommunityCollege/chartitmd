'use strict';
function asBinString() {
    let data = [];
    let result = '';
    for (let i = 0; i < 16; i++) {
        data.push(Math.floor(Math.random() * 255));
    }
    data[6] = data[6] & 0x0f | 0x40;
    data[8] = data[8] & 0x3f | 0x80;
    for (let i = 0; i < data.length; i++) {
        result += String.fromCharCode(data[i]);
    }
    return result;
}
function asBase64() {
    let data = asBinString();
    let result = '';
    let binary = '0000';
    for (let i = 0; i < data.length; i++) {
        let char = (
            '00000000' + data.charCodeAt(i)
                             .toString(2)
        ).slice(-8);
        // console.log(char);
        binary += char;
    }
    for (let i = 0; i < binary.length; i += 6) {
        let idx = binary.substr(i, 6);
        result += base64[idx];
    }
    return result;
}
